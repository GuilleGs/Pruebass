package Modelos;

public class IDcaso {
    private String rut;
    private int iterador;

    public IDcaso(String rut, int iterador) {
        this.rut = rut;
        this.iterador = iterador;
    }

    public String getRut() {
        return rut;
    }

    public int getIterador() {
        return iterador;
    }
}
